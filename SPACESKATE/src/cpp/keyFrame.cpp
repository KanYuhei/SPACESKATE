//*************************************************************************************************
// コンストラクタ
//*************************************************************************************************
CKeyFrame::CKeyFrame()
{
    m_Frame = 0;
}

//*************************************************************************************************
// デコンストラクタ
//*************************************************************************************************
CKeyFrame::~CKeyFrame()
{
}

//*************************************************************************************************
// キークラスの設定
//*************************************************************************************************
void CKeyFrame::SetKey(CKey Key)
{
    m_Key.push_back(Key);
}

//*************************************************************************************************
// フレーム数の設定
//*************************************************************************************************
void CKeyFrame::SetFrame(int Frame)
{
    m_Frame = Frame;
}

//*************************************************************************************************
// キークラスの取得
//*************************************************************************************************
CKey *CKeyFrame::GetKey(int Index)
{
    return &m_Key[Index];
}

//*************************************************************************************************
// フレーム数の取得
//*************************************************************************************************
int CKeyFrame::GetFrame(void)
{
    return m_Frame;
}