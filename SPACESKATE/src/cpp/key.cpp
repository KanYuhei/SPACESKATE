//*************************************************************************************************
// コンストラクタ
//*************************************************************************************************
CKey::CKey()
{
    m_Position = D3DXVECTOR3(0.0f, 0.0f, 0.0f);
    m_Rotation = D3DXVECTOR3(0.0f, 0.0f, 0.0f);
}

//*************************************************************************************************
// デコンストラクタ
//*************************************************************************************************
CKey::~CKey()
{
}

//*************************************************************************************************
// 座標の設定
//*************************************************************************************************
void CKey::SetPos(D3DXVECTOR3 Position)
{
    m_Position = Position;
}

//*************************************************************************************************
// 回転の設定
//*************************************************************************************************
void CKey::SetRot(D3DXVECTOR3 Rotation)
{
    m_Rotation = Rotation;
}

//*************************************************************************************************
// 座標の取得
//*************************************************************************************************
D3DXVECTOR3 CKey::GetPos(void)
{
    return m_Position;
}

//*************************************************************************************************
// 回転の取得
//*************************************************************************************************
D3DXVECTOR3 CKey::GetRot(void)
{
    return m_Rotation;
}