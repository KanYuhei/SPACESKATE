//*****************************************************************************
// 入力処理の終了処理
//*****************************************************************************
void CInput::Uninit(void)
{
    if(m_pDInput)
    {// DirectInputオブジェクトの開放
        m_pDInput->Release();
        m_pDInput = NULL;
    }
}