//*************************************************************************************************
// コンストラクタ
//*************************************************************************************************
CMotion::CMotion()
{
    m_Play = false;
    m_Frame = 0;
    m_Key = 0;
    m_NumKey = 0;
    m_Loop = false;
}

//*************************************************************************************************
// デコンストラクタ
//*************************************************************************************************
CMotion::~CMotion()
{
}

//*************************************************************************************************
// 自身を生成
//*************************************************************************************************
CMotion *CMotion::Create(std::vector<CKeyFrame> KeyFrame,int NumKey,bool Loop)
{
    CMotion *pMotion;
    pMotion = new CMotion();
    pMotion->m_KeyFrame = KeyFrame;
    pMotion->m_NumKey = NumKey;
    pMotion->m_Loop = Loop;

    return pMotion;
}

//*************************************************************************************************
// 再生フラグの設定
//*************************************************************************************************
void CMotion::SetPlay(bool flg)
{
    m_Play = flg;
}

//*************************************************************************************************
// 現在のフレーム数の設定
//*************************************************************************************************
void CMotion::SetFrame(float frame)
{
    m_Frame = frame;
}

//*************************************************************************************************
// 現在のフレーム数の設定
//*************************************************************************************************
void CMotion::SetKey(int key)
{
    m_Key = key;
}

//*************************************************************************************************
// キーフレームクラスの取得
//*************************************************************************************************
CKeyFrame CMotion::GetKeyFrame(int key)
{
    return m_KeyFrame[key];
}

//*************************************************************************************************
// キーの数の取得
//*************************************************************************************************
int CMotion::GetNumKey(void)
{
    return m_NumKey;
}

//*************************************************************************************************
// ループフラグの取得
//*************************************************************************************************
bool CMotion::GetLoop(void)
{
    return m_Loop;
}

//*************************************************************************************************
// 再生フラグの取得
//*************************************************************************************************
bool CMotion::GetPlay(void)
{
    return m_Play;
}

//*************************************************************************************************
// 現在のフレーム数の取得
//*************************************************************************************************
float CMotion::GetFrame(void)
{
    return m_Frame;
}

//*************************************************************************************************
// 現在のキーの取得
//*************************************************************************************************
int CMotion::GetKey(void)
{
    return m_Key;
}