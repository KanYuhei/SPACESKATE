class CMotion
{
public:
    CMotion();                              //コンストラクタ
    ~CMotion();                             //デコンストラクタ

    void SetPlay(bool flg);                 //再生フラグの設定
    void SetFrame(float frame);             //現在のフレーム数の設定
    void SetKey(int key);                   //現在のキーの設定

    CKeyFrame GetKeyFrame(int key);         //キーフレームクラスの取得
    int GetNumKey(void);                    //キーの数の取得
    bool GetLoop(void);                     //ループフラグの取得
    bool GetPlay(void);                     //再生フラグの取得
    float GetFrame(void);                   //現在のフレーム数の取得
    int GetKey(void);                       //現在のキーの取得

    static CMotion *Create(std::vector<CKeyFrame> KeyFrame,
                           int NumKey, 
                           bool Loop);      //自身を生成

private:
    std::vector<CKeyFrame> m_KeyFrame;      //キーフレームクラス
    int m_NumKey;                           //キーの数
    bool m_Loop;                            //ループフラグ
    bool m_Play;                            //再生(ON:true,OFF:false)
    float m_Frame;                          //現在のフレーム数
    int m_Key;                              //現在のキー
};

